
public class TiposNumericos {

	public static void main(String[] args) {
		Byte numeroByte = 127; //-128 até 127
		
		Short numeroShort = -32768; //-32768 até 32767
		
		Integer numeroInteger = 1; //-2.147.483.648 até 2.147.483.647  MAIS UTILIZADOS
		
		Long numeroLong = 1L; // 9.223.372.036.854.775.808 até 9.223.372.036.854.775.807
		
		Float numeroFloat = 1.0f;
		
		Double numeroDouble = 1.0; // MAIS USADOS
	}
}
